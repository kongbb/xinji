CREATE TABLE [dbo].[RC_TRANSACTION] (
    [TRANSACTION_ID]   BIGINT          IDENTITY (1, 1) NOT NULL,
    [ACCOUNT_ID]       BIGINT          NULL,
    [PAYMENT_TYPE]     INT             NULL,
    [TRANSACTION_TIME] BIGINT          NULL,
    [SUB_TOTAL]        DECIMAL (18, 2) NULL,
    [TOTAL_TAX]        DECIMAL (18, 2) NULL,
    [TOTAL_DISCOUNT]   DECIMAL (18, 2) NULL,
    [TOTAL_PRICE]      DECIMAL (18, 2) NULL,
    [GRATUITY_AMOUNT]  DECIMAL (18, 2) NULL,
    [TERMINAL]         INT             NULL,
    [NOTE]             NCHAR (100)     NULL,
    [REASON_ID]        INT             NULL,
	[MODIFIED_TIME]    ROWVERSION      NOT NULL,
    [MODIFIED_BY]      BIGINT          NULL,
    CONSTRAINT [PK_RC_TRANSACTION] PRIMARY KEY CLUSTERED ([TRANSACTION_ID] ASC),
	CONSTRAINT [FK_RC_TRANSACTION_RC_ACCOUNTS] FOREIGN KEY ([ACCOUNT_ID]) REFERENCES [dbo].[RC_ACCOUNTS] ([ACCOUNT_ID]),
);
